
public class C6_제어문02 {

	public static void main(String[] args) {
		
		int korScore = 95;
		if(korScore>=90) {
			System.out.println("A");
		}else if(korScore>=80) {
			System.out.println("B");
		}else if(korScore>=70) {
			System.out.println("C");
		}else {
			System.out.println("F");
		}
		
		
		//if제어문 주의할 점
		//1. 한 if문에서 if를 여러번 쓸 수 없음 (별개의if문으로 인식함)
		
	}

}
